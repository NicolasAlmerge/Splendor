package ca.mcgill.nalmer.splendormodels;

/**
 * Represents the chosen game extension. The values are {@link GameExtension#BASE_ORIENT},
 * {@link GameExtension#BASE_ORIENT_CITIES} and {@link GameExtension#BASE_ORIENT_TRADING_POSTS}.
 */
public enum GameExtension {
  /**
   * Base and orient.
   */
  BASE_ORIENT,

  /**
   * Base, orient and cities.
   */
  BASE_ORIENT_CITIES,

  /**
   * Base, orient and trading posts.
   */
  BASE_ORIENT_TRADING_POSTS
}
