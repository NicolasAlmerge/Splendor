package ca.mcgill.nalmer.lsutilities.model;

/**
 * Represents the user's role. The three user roles are {@link #ROLE_PLAYER}, {@link #ROLE_ADMIN}
 * and {@link #ROLE_SERVICE}.
 *
 * @author Nicolas Almerge
 * @since 1.0
 */
public enum UserRole {
  /**
   * User role.
   */
  ROLE_PLAYER,

  /**
   * Admin role.
   */
  ROLE_ADMIN,

  /**
   * Service role.
   */
  ROLE_SERVICE
}
